public class BeispielVariablen01 {

  public static void main(String[] args) {

    int meineZahl;
    meineZahl = 5;

    System.out.println(meineZahl + 10);
    System.out.println(meineZahl - 10);
    System.out.println(meineZahl * 4);
    System.out.println(meineZahl / 2);
    System.out.println(meineZahl % 2);
    System.out.println();

    meineZahl = 100;

    int meineZweiteZahl;
    meineZweiteZahl = 20;

    System.out.println(meineZahl + meineZweiteZahl);
    System.out.println(meineZahl - meineZweiteZahl);
    System.out.println(meineZahl * meineZweiteZahl);
    System.out.println(meineZahl / meineZweiteZahl);
    System.out.println(meineZahl % meineZweiteZahl);
    System.out.println();

    System.out.println(meineZahl);
    System.out.println(meineZweiteZahl);

  }

}
