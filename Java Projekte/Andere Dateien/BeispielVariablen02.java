public class BeispielVariablen02 {

  public static void main(String[] args) {

    int meineZahl;
    meineZahl = 5;

    meineZahl = meineZahl + 10;
    System.out.println(meineZahl);

    meineZahl = meineZahl * 2;
    System.out.println(meineZahl);

    meineZahl = meineZahl / 3;
    System.out.println(meineZahl);

    meineZahl = meineZahl * meineZahl;
    System.out.println(meineZahl);

  }

}
